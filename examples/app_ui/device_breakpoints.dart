/// [DeviceBreakpoints] - A device breakpoint used for creating responsive
/// layouts in app
class DeviceBreakpoints {
  DeviceBreakpoints._();

  /// Max width for a small layout.
  static const double small = 360;

  /// Max width for a medium layout.
  static const double medium = 555;

  /// Max width for a large layout.
  static const double large = 620;
}
